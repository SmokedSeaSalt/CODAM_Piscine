#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		i;
	char	c;

	if (nb < 0)
	{
		nb = nb * -1;
		c = '-';
		write(1, &c, 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		i = (nb % 10);
		c = '0' + i;
		write(1, &c, 1);
	}
	else
	{
		c = '0' + nb;
		write(1, &c, 1);
	}
}
