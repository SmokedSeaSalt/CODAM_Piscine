int	ft_str_is_numeric(char *str)
{
	int	i;
	int	c;
	int	flag;

	i = 0;
	flag = 1;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!(c >= '0' && c <= '9'))
		{
			flag = 0;
		}
		i++;
	}
	return (flag);
}
