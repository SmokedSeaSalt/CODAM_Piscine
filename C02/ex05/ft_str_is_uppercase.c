int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	c;
	int	flag;

	i = 0;
	flag = 1;
	while (str[i] != '\0')
	{
		c = str[i];
		if (!(c >= 'A' && c <= 'Z'))
		{
			flag = 0;
		}
		i++;
	}
	return (flag);
}
